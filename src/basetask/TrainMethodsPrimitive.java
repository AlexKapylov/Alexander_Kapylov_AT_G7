package basetask;

public class TrainMethodsPrimitive {

    public void printInt(int numb) {
        System.out.println("я получил на вход число " + numb);
    }

    public void printLong(long lNumb) {
        System.out.println("я получил на вход  длинное число " + lNumb);
    }

    public void printChar(char ch) {
        System.out.println("я получил на вход символ " + ch);
    }

    public void printFloat(float fl) {
        System.out.println("я получил на вход дробное число " + fl);
    }

    public void printDouble(double dbl) {
        System.out.println("я получил на вход длинное дробное число " + dbl);
    }

    public void printShort(short sh) {
        System.out.println("я получил на вход короткое число " + sh);
    }

    public void printByte(byte bt) {
        System.out.println("я получил на вход очень короткое число " + bt);
    }

    public void printBoolean(boolean bl) {
        System.out.println("я получил на вход булево " + bl);
    }

}
